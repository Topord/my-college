import math
import turtle as t

t.hideturtle()
t.speed(0)
t.screensize(800,800)
t.setup(800,800)
t.tracer(False)

def sin(move):
    i=-218
    t.penup()
    t.goto(-318,299.2-move)
    t.pendown()
    while(i<=-4):
        i = i + 0.1
        y = -60*math.sin(0.06*i+0.5)
        t.goto(i-100,y+300-move)
def x_arrow(move):
    t.pensize(3)
    t.penup()
    t.goto(-90,299.2-move)
    t.pendown()
    t.seth(160)
    t.fd(10)
    t.seth(-20)
    t.fd(10)
    t.seth(-160)
    t.fd(10)
    t.pensize(1)
def y_arrow(move):
    t.pensize(3)
    t.penup()
    t.goto(-213,382.2-move)
    t.pendown()
    t.seth(-70)
    t.fd(10)
    t.seth(110)
    t.fd(10)
    t.seth(-110)
    t.fd(10)
    t.pensize(1)
def axis(move):
    t.penup()
    t.goto(-318,299.2-move)
    t.pendown()
    t.seth(0)
    t.fd(228)
    t.seth(180)
    t.fd(124)
    t.seth(90)
    t.fd(80)
    t.seth(-90)
    t.fd(160)
def all_axis():
    x_arrow(0)
    y_arrow(0)
    x_arrow(250)
    y_arrow(250)
    x_arrow(500)
    y_arrow(500)
    axis(0)
    axis(250)
    axis(500)
sin(0)
sin(250)
sin(500)
all_axis()
t.update()

#汉字----------------------------------------------------------------------------------------------------------------------------
def write():
    t.speed(0)
    t.setup(800,600)
    t.pencolor('red')
    t.write('你有时候是不是特孤独？',move=True,align='center',font=('方正舒体',20,'normal'))
    t.penup()
    t.setheading(-90)
    t.forward(30)
    t.setheading(180)
    t.forward(50)
    t.setheading(0)
    t.pendown()
    t.write('世界上这么优秀的人就只有你一个！',move=True,align='center',font=('方正舒体',20,'normal'))
    t.hideturtle()
#表情-----------------------------------------------------------------------------------------------------------------------------
def face():
    t.speed(500)
    t.color('yellow','yellow')
    t.begin_fill()
    t.circle(50)
    t.end_fill()
    #嘴巴
    t.penup()
    t.setheading(90)
    t.forward(15)
    t.pendown()
    t.setheading(0)
    t.pensize(1)
    t.color('black')
    t.begin_fill()
    t.color('black','white')
    t.circle(13,360)
    t.end_fill()
    #眼白
    t.setheading(90)
    t.pencolor('yellow')
    t.penup()
    t.forward(35)
    t.setheading(180)
    t.forward(35)
    t.pendown()
    t.setheading(0)
    t.color('white')
    t.begin_fill()
    t.circle(15)
    t.end_fill()
    #眼球
    t.color('black')
    t.begin_fill()
    t.circle(10)
    t.end_fill()
    #眼白
    t.setheading(0)
    t.penup()

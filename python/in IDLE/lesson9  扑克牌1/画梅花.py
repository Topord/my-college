import turtle as t
t.speed(0)
t.hideturtle()
def plum1(b,i,j):#正
    t.penup()
    t.goto(i,j)
    t.pendown()
    t.color('black')
    t.begin_fill()
    t.setheading(90)
    t.circle(b/2,-60)
    t.setheading(0)
    t.forward(b/2)
    t.setheading(152)
    t.circle(-b/2,60)
    t.setheading(-60)#
    t.circle(b/3,240)
    t.setheading(60)
    t.circle(b/3,240)
    t.setheading(180)
    t.circle(b/3,240)
    t.end_fill()

import turtle as t
t.hideturtle()
t.setup(800,800)
t.screensize(900,900)
def frame():
    t.pencolor('black')
    t.penup()
    t.goto(250,0)
    t.pendown()
    t.setheading(90)
    t.forward(400)
    t.setheading(180)
    t.forward(500)
    t.setheading(-90)
    t.forward(800)
    t.setheading(0)
    t.forward(500)
    t.setheading(90)
    t.forward(400)
def heart1(a,i,j):#---正的爱心     a-->大小  i,j-->位置
    t.penup()
    t.goto(i,j)
    t.pendown()
    t.begin_fill()
    t.color('red')
    t.setheading(115)
    t.circle(a,200)
    t.forward(2*a)
    t.left(90)
    t.forward(2*a)
    t.circle(a,200)
    t.end_fill()
def heart2(r,m,n):#---反的爱心     b-->大小  m,n-->位置
    t.penup()
    t.goto(m,n)
    t.pendown()
    t.begin_fill()
    t.color('red')
    t.setheading(115)
    t.circle(-r,-200)
    t.forward(-2*r)
    t.left(-90)
    t.forward(2*r)
    t.circle(r,200)
    t.end_fill()
def H6x(x1,y1):#--'2'    x,y-->位置
    t.pencolor('red')
    t.pensize(2)
    t.penup()
    t.goto(x1,y1)
    t.pendown()
    t.write('6',move=False,align='left',font=('宋体',30,'normal'))
def H6y(x2,y2):#--'2'    x,y-->位置
    t.pencolor('red')
    t.pensize(3)
    t.penup()
    t.goto(x2,y2)
    t.pendown()
    t.write('9',move=False,align='left',font=('宋体',30,'normal'))

t.speed(0)
frame()
H6x(-200,360)
heart1(15,-200,320)
heart1(30,-125,195)
heart1(30,125,195)
heart1(30,-125,0)
heart1(30,125,0)
heart1(30,-125,-195)
heart1(30,125,-195)
heart2(15,200,-320)
H6y(200,-370)
